<h1>&Uuml;bungen hinzuf&uuml;gen</h1>
<p>Wenn du eine &Uuml;bung hinzuf&uuml;gen m&ouml;chtest, musst du zuerst im Menu (horizontaler, hellblauer
Balken) auf &quot;&Uuml;bungen&quot; klicken. Danach siehst du die &Uuml;bersicht aller bisher eingetragenen
&Uuml;bungen. 
</p>
<p>Nun befindet sich rechts oben ein Pluszeichen <img src="images/plus.png" style="height:17px" /> 
mit dem Text &quot;&Uuml;bung hinzuf&uuml;gen&quot;. Um eine &Uuml;bung zu erstellen musst du auf diesen
Schriftzug klicken. Danach &ouml;ffnet sich ein Fenster, wo du deine &Uuml;bung eintragen kannst.</p>
<p>Bei Bedarf kannst du auch ein Protokoll, das du auf deinem Computer im *.doc-, 
*.xls-, *.pdf- oder im *.txt Format gespeichert haben musst, mit deiner &Uuml;bung ver&ouml;ffentlichen.</p>
<p>Wenn du alle Angaben eingetragen hast, kannst du auf die Schaltfl&auml;che 
&quot;Absenden&quot; klicken. Danach wird die von dir eingegeben &Uuml;bung 
einem Administrator gesendet, der sie zuerst begutachten wird. Nach etwa einem 
Tag wird dann die &Uuml;bung definitiv für alle sichtbar gemacht.</p>
<p>Um eine &Uuml;bung wieder vom Internet zu entfernen, musst du pers&ouml;nlich (&uuml;ber 
das Kontaktformular) mit einem der Administratoren Kontakt aufnehmen.</p>


