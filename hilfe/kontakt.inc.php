<h1>Kritik hinterlassen</h1>
<p>Es ist uns stets wichtig, dass wir die <em>pfadinet</em>-Seite den Bed&uuml;rfnissen der Besucher
anpassen. Darum sind wir um jede Kritik froh.
</p>
<p>Um eine Kritik an uns zu senden, musst du im Menu (horizontaler, hellblauer Balken) auf
&quot;Kontakt&quot; klicken. Dort befindet sich ein Formular wo du dein Name, deine E-Mail-Adresse und 
deine Kritik (Nachricht) angeben kannst. Wir versuchen dann auch mit dir Kontakt aufzunehmen um 
Verbesserungsvorschl&auml;ge auszuarbeiten oder Missst&auml;nde zu beseitigen.</p>
<p>Wenn du auf &quot;Absenden&quot; geklickt hast, erscheint ein zweites Fenster, dass die 
Kritik gesendet wurde.</p>
<p>Wir sind froh, um jede Mitteilung, die wir von dir erhalten!</p>




