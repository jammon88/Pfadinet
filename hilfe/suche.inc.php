<h1>Übungen suchen</h1>
<p>Die &Uuml;bungssuche ist eines der h&ouml;chst gestelltesten Funktionen von <em>pfadinet</em>. Auf
jeder Seite findest du rechts ein Feld &quot;&uuml;bungssuche&quot;. Dort kannst du die Pfadi&uuml;bungen
suchen die genau auf deinen Samstag-Nachmittag zutreffen. Wenn es dir bei einem Feld keine Rolle spielt (z. 
B. wie viele Pfader, dass ben&ouml;tigt werden), dann bel&auml;sst du das Feld auf &quot;egal&quot;.
</p>
<p>Per Absendebutton kannst du das Formular absenden und es werden dir alle &Uuml;bungen zu deinen 
Suchkriterien angezeigt. Danach kannst du wie &uuml;blich bei jeder &Uuml;bung rechts oben auf
&quot;Details&quot; klicken, um genauere Infos zu derjenigen &Uuml;bung zu erfahren. 
</p>
<p>Du kannst dir auch alle &Uuml;bungen anzeigen lassen, indem du im Menu (horizontaler, hellblauer Balken)
auf &quot;&Uuml;bungen&quot; klickst. Danach kannst du die &Uuml;bungen auch nach Bewertung, Beliebtheit, 
Aufrufen oder Datum sortieren lassen (Formular gerade unter dem Titel &quot;&Uuml;bungen&quot;)</p>
<p>Die beliebtesten 10 &Uuml;bungen werden zus&auml;tzlich rechts im Feld &quot;top10 - &uuml;bungen&quot;
angezeigt. Die neusten zehn &Uuml;bungen im Feld &quot;neueste &uuml;bungen&quot;</p>



