<h1>Benüzung der Hilfe</h1>
<p>Die Hilfe soll dir erkl&auml;ren, wie du dich auf dem <em>pfadinet</em> bewegen kannst. Die Hilfe findest
du, indem du auf dieses Fragezeichen (Beim Menu am rechten Bildschirmrand) klickst:<br>
<br>
<img src="images/help.png" style="height:45px" /></p>
<p>Nach dem Klick sollte sich ein neues Fenster &ouml;ffnen. Falls aber nichts passiert, musst du deinen
Pop-up-Blocker ausschalten. Dazu musst du in die Einstellungen deines Browsers wechseln.</p>
<p>Nun bist du in der Hilfe angelangt. In der linken Spalte findest du die 
Hilfethemen. Wenn du bei einem Thema nicht weiss, wie es funktioniert, klick auf 
dasjenige Thema und die Vorg&auml;nge werden dir in der rechten Spalte erkl&auml;rt.</p>
<p>Um die Hilfe wieder schliessen möchtest, musst du auf das Schliessen-Symbol 
oben rechts (<img src="images/close.png" style="height:22px"> <span style="color:#FA9C39; text-decoration:underline">Hilfe schliessen</span>) 
klicken. Falls das nicht geht, hast du Javascript desaktiviert. Aktiviere 
Javascript oder schliesse das Hilfe-Fenster per Schliess-Button deines Browsers.</p>


